#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (n > i)
	{
		if (((const unsigned char *)s)[i] == (unsigned char)c)
			return ((void *)((const char *)s + i));
		i++;
	}
	return (0);
}
