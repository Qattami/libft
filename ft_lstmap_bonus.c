#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*head;
	t_list	*node;
	void	*p;

	if (!lst || !f || !del)
		return (NULL);
	head = NULL;
	while (lst)
	{
		p = f(lst->content);
		node = ft_lstnew(p);
		if (!node)
		{
			free(p);
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, node);
		lst = lst->next;
	}
	return (head);
}
